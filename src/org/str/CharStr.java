package org.str;


public class CharStr {
	public static void main(String[] args) {
		
		String s= "Hello welcome world";
		
		char[] ch = s.toCharArray();
		
		int count=0;
		
		for (char d : ch) {
			
			
			
			
			if (d=='a'|| d=='e'||d=='i'||d=='o'||d=='u') {
				
				
				count++;
				
				System.out.println(d+""+""+count);
				
				
			} 

			
			
			
		}
		

		
		
		
		
		
		
			
		}
		
		
	
		
	}
	
	


